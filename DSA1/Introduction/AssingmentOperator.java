package Introduction;

public class AssingmentOperator {
    public static void main(String args[]){
        int a=100;
        a += 200;

        System.out.println(a);
        int b=500;
        b-=300;
        System.out.println(b);
        int c=600;
        c*=2;
        System.out.println(c);
        int d=100;
        d%=50;
        System.out.println(d);
        int e=1000;
        e/=2;
        System.out.println(e);
    }
}
